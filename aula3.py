# Tipos int e float 
# int -> Números inteiros
# O tipo do int representa qualquer número
# Positivo ou negativo. int sem sinal é considerado positivo
print(23) #int
print(-11) #int 
print(0) #int
print()

# float -> Número com ponto flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal sinal é coniderado positivo.
print(1.1, 10.11) #float
print(0.0, -5.1) #float
print()

# A função type mostra o tipo que o python inferiu o valor.
print(type('vinicius'))
print(type(4))
print(type(4.5))
print(type(4.5), type(5), type('Olá'), type(True))
