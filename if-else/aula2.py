# if, elif e else: entendendo o fluxo do interpretador em condicionais
# if /    elif    / else
# se / se não se / se não

condicao1 = False
condicao2 = False
condicao3 = True
condicao4 = True

if condicao1: 
    print('Codigo para condição 1')
    print('Codigo para condição 1')
elif condicao2:
    print('Codigo para condição 2')
elif condicao3:
    print('Codigo para condição 3')
elif condicao4:
    print('Codigo para condição 4')
else:
    print('Este é o codigo de ELSE')
