#Introdução aos blocos de código + if / elif / else (condicionais)
# if /    elif    / else
# se / se não se / se não

entrada = input('Voce quer "Entrar" ou "Sair"? ')

if entrada == "entrar":
    print('Voce entrou no sistema')
elif entrada == "sair":
    print('Voce saiu do sistema')
else:
    print('Voce não digitou nem entrar nem sair')
